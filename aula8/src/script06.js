//Exercício 5: Calcule a área de um retángulo de acordo com os dados informados pelo usuário.

alert("Área de retangulo");

let baseRetangulo = parseFloat(prompt("Informe o valor da base do retangulo: "));

let alturaRetangulo = parseFloat(prompt("Informe o valor da altura do retangulo: "));

let areaRetangulo = baseRetangulo * alturaRetangulo;

alert("A área total do retangulo é: " + areaRetangulo);